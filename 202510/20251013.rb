# 🧩 問題
# 商品データの配列があります。
# 価格（:price）の安い順に並べ替えてください。
#
# 💡 期待する出力（例）
# [{:name=>"みかん", :price=>100}, {:name=>"りんご", :price=>200}, {:name=>"ぶどう", :price=>300}]

items = [
  { name: "ぶどう", price: 300 },
  { name: "りんご", price: 200 },
  { name: "みかん", price: 100 }
]

# ここに処理を書く
p items.sort_by{|item| item[:price]}