# 🧩 問題
# 動物の名前が入った配列があります。
# 各動物が何回登場したかを数えて、ハッシュで返してください。
#
# 💡 期待する出力（例）
# {"犬"=>3, "猫"=>2, "鳥"=>1}

animals = ["犬", "猫", "犬", "鳥", "犬", "猫"]

# ここに処理を書く
p animals.group_by{|animal| animal}.transform_values{|i| i.count}

